#include <stdio.h>

int main(int argc, char const *argv[])
{
    int a, b;

    printf("Enter the First Number:\n");
    scanf("%d", &a);
    printf("Enter the Second Number:\n");
    scanf("%d", &b);

    printf("%d + %d = %d\n", a, b, a + b);
    return 0;
}
